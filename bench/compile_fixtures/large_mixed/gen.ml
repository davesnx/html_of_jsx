(* Generator for large_mixed.mlx.

   The fixture is checked in (not generated at build time) so that
   compile_bench measures only mlx-pp + ppx + ocaml compilation, without a
   generator step polluting the timings after each [dune clean].

   Regenerate with:

     ocaml bench/compile_fixtures/large_mixed/gen.ml > \
       bench/compile_fixtures/large_mixed/large_mixed.mlx
*)

let components = 800

let header =
  {|(* GENERATED by gen.ml - do not edit by hand.

   Large compile-time fixture: exercises the four main PPX codegen paths
   (fully static, dynamic attributes, optional attributes, dynamic children)
   at a scale where per-attribute-lookup regressions in the PPX are clearly
   visible in compile_bench results. *)
|}

let static_component i =
  Printf.sprintf
    {|let c%d () =
  <section class_="card static">
    <header class_="card-header">
      <h2 class_="title">(JSX.string "Static title %d")</h2>
    </header>
    <div class_="body">
      <p class_="text">(JSX.string "Lorem ipsum dolor sit amet %d")</p>
      <a href="/permalink/%d" class_="link" target=`_blank rel="noopener">
        (JSX.string "Read more")
      </a>
    </div>
  </section>
|}
    i i i i

let dynamic_attrs_component i =
  Printf.sprintf
    {|let c%d ~name ~count =
  <div
    class_="row"
    id=name
    tabindex=count
    data_index=(Int.to_string count)
    title=name>
    <span class_="name">(JSX.string name)</span>
    <span class_="count">(JSX.int count)</span>
  </div>
|}
    i

let optional_attrs_component i =
  Printf.sprintf
    {|let c%d ?id ?title ~label () =
  <button ?id ?title class_="btn" disabled=false>(JSX.string label)</button>
|}
    i

let dynamic_children_component i =
  Printf.sprintf
    {|let c%d ~items =
  <ul class_="list">
    (JSX.list
       (List.map (fun item -> <li class_="item">(JSX.string item)</li>) items)
    )
  </ul>
|}
    i

let () =
  print_string header;
  for i = 0 to components - 1 do
    print_newline ();
    let component =
      match i mod 4 with
      | 0 ->
          static_component i
      | 1 ->
          dynamic_attrs_component i
      | 2 ->
          optional_attrs_component i
      | _ ->
          dynamic_children_component i
    in
    print_string component
  done
